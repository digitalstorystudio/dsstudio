# Regex to match the old nav block (from <div class="main-website"> to just before <main or first <section)
# We'll find the nav block start and end markers and replace

$pages = @(
    "destination-wedding.html",
    "creative-shoot.html",
    "events.html",
    "live-streaming.html",
    "model-photoshoot.html",
    "others.html",
    "same-day-edit.html",
    "testimonials.html",
    "wedding-highlights-teaser.html"
)

$scriptDir = Split-Path -Parent $MyInvocation.MyCommand.Path

foreach ($page in $pages) {
    $filePath = Join-Path $scriptDir $page
    if (-not (Test-Path $filePath)) {
        Write-Host "SKIPPING (not found): $page"
        continue
    }

    $content = Get-Content $filePath -Raw -Encoding UTF8

    # Find the start of the nav block  
    $navStart = $content.IndexOf('<div class="main-website">')
    if ($navStart -lt 0) {
        Write-Host "WARNING: No main-website div found in $page"
        continue
    }

    # Find where main content starts (either <main or the first <section after nav)
    # Look for </nav> then find the next meaningful tag
    $navEnd = $content.IndexOf('</nav>', $navStart)
    if ($navEnd -lt 0) {
        Write-Host "WARNING: No </nav> found in $page"
        continue
    }
    $navEnd = $navEnd + 6  # skip past </nav>

    # Skip whitespace/newlines after </nav>
    $afterNav = $navEnd
    while ($afterNav -lt $content.Length -and ($content[$afterNav] -eq "`r" -or $content[$afterNav] -eq "`n" -or $content[$afterNav] -eq " ")) {
        $afterNav++
    }

    # Replace the old nav block
    $before = $content.Substring(0, $navStart)
    $after = $content.Substring($afterNav)
    $content = $before + $FULL_NAV + "`n" + $after

    [System.IO.File]::WriteAllText($filePath, $content, [System.Text.Encoding]::UTF8)
    Write-Host "DONE: $page"
}

Write-Host "`nAll pages updated with full nav!"
